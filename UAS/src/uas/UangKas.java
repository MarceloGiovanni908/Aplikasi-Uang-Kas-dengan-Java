    private void Uangkas_TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Uangkas_TableMouseClicked

        int i = Uangkas_Table.getSelectedRow();
        if(i==-1){
        return;
        }
        String code = (String)Uangkas_Table.getValueAt(i,0);
        String code1 = (String)Uangkas_Table.getValueAt(i,1);
        String code2 = (String)Uangkas_Table.getValueAt(i,2);
        String code3 = (String)Uangkas_Table.getValueAt(i,3);
        String code4 = (String)Uangkas_Table.getValueAt(i,4);
        String code5 = (String)Uangkas_Table.getValueAt(i,5);
        String code6 = (String)Uangkas_Table.getValueAt(i,6);
//        String code7 = (String)Uangkas_Table.getValueAt(i,7);

        
        Uangkas_No1.setText(code);
        Uangkas_Tanggal1.setDateFormatString(code1);
        Uangkas_Nama1.setText(code2);
        Uangkas_Pembayaran.setText(code3);
        Uangkas_Pengeluaran.setText(code4);
        Uangkas_SaldoAkhir1.setText(code5);
        Uangkas_Keterangan.setText(code6);
    }//GEN-LAST:event_Uangkas_TableMouseClicked

